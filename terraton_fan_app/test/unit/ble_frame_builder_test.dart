// test/unit/ble_frame_builder_test.dart
import 'package:flutter_test/flutter_test.dart';
import 'package:terraton_fan_app/core/commands/command_loader.dart';
import 'package:terraton_fan_app/core/ble/ble_frame_builder.dart';

void main() {
  setUpAll(() async {
    TestWidgetsFlutterBinding.ensureInitialized();
    await CommandLoader.load();
  });

  // Status poll is a fixed non-standard frame — checksum is NOT derived from buildFrame().
  // Checksum uses include-header formula: (0x55+0xAA+0x00+0x00+0x01+0x00) & 0xFF = 0x00.
  test('statusPoll', () =>
      expect(BleFrameBuilder.statusPoll(), [0x55, 0xAA, 0x00, 0x00, 0x01, 0x00, 0x00]));

  // All frames below: checksum = (sum of ALL bytes before checksum) & 0xFF
  test('powerOn',  () =>
      expect(BleFrameBuilder.powerOn(),  [0x55, 0xAA, 0x06, 0x02, 0x01, 0x01, 0x09]));

  test('powerOff', () =>
      expect(BleFrameBuilder.powerOff(), [0x55, 0xAA, 0x06, 0x02, 0x01, 0x00, 0x08]));

  test('setSpeed(1)', () => expect(BleFrameBuilder.setSpeed(1), [0x55, 0xAA, 0x06, 0x04, 0x01, 0x01, 0x0B]));
  test('setSpeed(2)', () => expect(BleFrameBuilder.setSpeed(2), [0x55, 0xAA, 0x06, 0x04, 0x01, 0x02, 0x0C]));
  test('setSpeed(3)', () => expect(BleFrameBuilder.setSpeed(3), [0x55, 0xAA, 0x06, 0x04, 0x01, 0x03, 0x0D]));
  test('setSpeed(4)', () => expect(BleFrameBuilder.setSpeed(4), [0x55, 0xAA, 0x06, 0x04, 0x01, 0x04, 0x0E]));
  test('setSpeed(5)', () => expect(BleFrameBuilder.setSpeed(5), [0x55, 0xAA, 0x06, 0x04, 0x01, 0x05, 0x0F]));
  test('setSpeed(6)', () => expect(BleFrameBuilder.setSpeed(6), [0x55, 0xAA, 0x06, 0x04, 0x01, 0x06, 0x10]));

  // setNormal: cmd=0x21 data=0x00; sum=(0x55+0xAA+0x06+0x21+0x01+0x00)&0xFF=0x27
  test('setNormal',  () => expect(BleFrameBuilder.setNormal(),  [0x55, 0xAA, 0x06, 0x21, 0x01, 0x00, 0x27]));
  test('setBoost',   () => expect(BleFrameBuilder.setBoost(),   [0x55, 0xAA, 0x06, 0x21, 0x01, 0x01, 0x28]));
  test('setNature',  () => expect(BleFrameBuilder.setNature(),  [0x55, 0xAA, 0x06, 0x21, 0x01, 0x02, 0x29]));
  test('setReverse', () => expect(BleFrameBuilder.setReverse(), [0x55, 0xAA, 0x06, 0x21, 0x01, 0x03, 0x2A]));
  test('setSmart',   () => expect(BleFrameBuilder.setSmart(),   [0x55, 0xAA, 0x06, 0x21, 0x01, 0x04, 0x2B]));
  test('timerOff',   () => expect(BleFrameBuilder.timerOff(),   [0x55, 0xAA, 0x06, 0x22, 0x01, 0x00, 0x28]));
  test('timer2h',    () => expect(BleFrameBuilder.timer2h(),    [0x55, 0xAA, 0x06, 0x22, 0x01, 0x02, 0x2A]));
  test('timer4h',    () => expect(BleFrameBuilder.timer4h(),    [0x55, 0xAA, 0x06, 0x22, 0x01, 0x04, 0x2C]));
  test('timer8h',    () => expect(BleFrameBuilder.timer8h(),    [0x55, 0xAA, 0x06, 0x22, 0x01, 0x08, 0x30]));
  test('queryPower', () => expect(BleFrameBuilder.queryPower(), [0x55, 0xAA, 0x06, 0x23, 0x01, 0x00, 0x29]));
  test('querySpeed', () => expect(BleFrameBuilder.querySpeed(), [0x55, 0xAA, 0x06, 0x24, 0x01, 0x00, 0x2A]));
  test('lightOn returns null', () => expect(BleFrameBuilder.lightOn(),  isNull));
  test('lightOff returns null', () => expect(BleFrameBuilder.lightOff(), isNull));
}
